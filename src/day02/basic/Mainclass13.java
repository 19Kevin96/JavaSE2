package day02.basic;
/*
 * do ~ while()  문 
 * 
 * do{
 *  ..반복 수행할 문징
 * }while(조건문);
 * 
 * 
 * 
 * 
 */
public class Mainclass13 {
	public static void main(String[] args) {
		int count = 0;
		do {		// 조건이 잘못된 식도 최소 한번은 실행함.
			System.out.println(count+" ");
			count++; 		
			}while(count<100);
		}
	


}





















